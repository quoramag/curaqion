				<h2>3. Keep It Simple</h2>
				<p>Using too many colors is a common mistake in design. It is usually best to use one prominent color in design and offset it with a neutral color like white, gray or black. Using too many colors can be like trying to convey a million feelings and messages at once, which can confuse the person viewing your design.</p>

				<h2>4. Contrast</h2>
				<p>For the most part, dark colors work best with bright colors. This is why most books are designed using white backgrounds and black text.</p>

				<p>Each color has a contrast value. White is the lightest value and black is the darkest value.</p>

				<p>Yellow and green have very light values. So using yellow or green text on a white background would be difficult to read. Yellow would work well on a dark grey or black background, which would explain why most road warning signs are yellow and black.</p>

<p><img src="http://qph.cf.quoracdn.net/main-qimg-4b113b16b2ee1f333f580173d1506367" />
</p>
<p>Blue and purple have dark color values, so these would work well on a white background, but not so good on a dark background</p>
