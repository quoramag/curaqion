<p class="intro">There's no such thing as "surgery to turn white". Your basal skin color is determined by the density and the production levels of cells within your skin called melanocytes that produce the melanin that give you whatever colour you have when all outside factors like sun exposure are kept out of the equation.</p>

Understanding this basic concept will give you the following insights:

    A "surgery" to turn white is useless; unless you'd want to strip one's skin off, which seems pretty extreme to me.
    When light-coloured people go in the sun, ultraviolet light activates these melanocytes and induces more melanin production. That's "tanning" (try to disregard the orange tone of artificial spray paint tanning).

    Some people are born with a mutation that completely impairs the production of melanin. This is called albinism. Albinism is colour-blind (the girls below are both from the same African descent) and widespread through mammals.


    Other people, like Michael Jackson, have an auto-immune disease in which patches of your skin lose their melanocytes, essentially losing any ability to produce the basic colour your healthy skin has. This is Vitiligo, as Kat Sommers explains. In Michael Jackson's case, this was already apparent in the early years, contrary to popular belief.

Mr. Jackson apparently chose to mask the lesions by making his overall skin tone as light as possible, which is not an unusual approach.
Although it is very hard to tell what is true or not with the level of gossip and speculation a star of that level attracted and still attracts, Mr. Jackson was a person in the spotlights and he came from a poor family trying to break with poverty through the world of show business; the way he looked was paramount in becoming famous and possibly caused him to worry about the disease and its implications more than a mere mortal would.

This has possibly led to an obsession with the way he looked and resulted in him trying all sorts of experimental and/or alternative treatments that might have further harmed his skin.

But surgery? Very unlikely.

I'm not saying he didn't undergo plastic surgeries by the way, you don't need to be an expert to see that he did.
I'm just saying it was not to turn white.
