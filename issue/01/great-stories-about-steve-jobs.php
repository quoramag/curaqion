<p>Founder of Apple, Steve Jobs, passed away today. I never met the man personally and now I never will. I only had one semi-personal interaction with Steve Jobs and I thought it was a good time to share it.</p>

<p>Before I met my wife, I had a girlfriend named Rebecca. Rebecca had non-Hodgkin's lymphoma. It was a rough time in her life and she was very depressed by it, even though chemotherapy was healing her over time.</p>

<p>Rebecca was a big fan of Pixar films. I knew Steve Jobs had also been battling cancer and was a big part of the Pixar company. I didn’t know a lot about the guy at this time as it was before the rise of the iPod, iPhone, or iPad. I also wasn’t the thorough nerd that I am now. Yes, there was a time I didn’t care about technology.</p>

<p>I sent a letter to Steve Jobs telling him about Rebecca and her situation. I asked for an autograph for her, hoping that could be something positive for her and encourage some positivity. I never thought I would get a reply, but I thought it was worth a try.</p>

<p>A week later I receive a package in the mail. In this thick envelope was a letter from Steve Jobs speaking of his cancer fight and how he wished Rebecca a quick recovery. Also in this envelope was six Pixar prints signed by John Lasseter, Andrew Stanton, Pete Docter, and Joe Ranft (a fellow cancer sufferer). Each of these men had written a letter to Rebecca wishing her well.</p>

<p>Jobs did not have to go to this kind of trouble, but he did anyway. Steve Jobs was not a man known for his public charity and many people think he was driven by selfishness and greed. But this act goes against that idea for me. This was most certainly a positive, selfless, and charitable act.</p>

<p>Joe Ranft passed away in a horrible car crash a few years ago and Jobs died today. This story is how I will remember Steve Jobs. Not for the technological advances he promoted. Not for the leaps and bounds that he funded. Not for the boundaries pushed. Of course our world would not be as advanced as it is now without him. Without Jobs we would not have personal computers in almost every home. We wouldn’t have intuitive personal tech devices. Sure, other companies came up with the technology, but Apple made it easy.</p>

<p>I was blown away by this when it happened. Rebecca treasured these letters and autographs and they infused positivity into her life. Eventually Rebecca recovered through chemo and radiotherapy. I don’t know if she even remembers getting this package (she was never much of a movie freak as me), but I know she has the letters somewhere. I know they meant something at the time. I know they mean something to me now.</p>



