<p class="intro">I recently concluded a 4 month adventure of living from my car in Silicon Valley. Don't listen to the naysayers. It can be done, and it will save you a ton of money. I did this out of choice, also while bootstrapping my startup.</p>

<p>Here's what Inc. Magazine had to say about it: http://www.inc.com/magazine/2012...</p>

<p>My monthly costs were a grand total of $219. $100 for a 24/7 co-working membership, $39 for a 24/7 gym membership, and $80 at the grocery store. Here's how the logistics of it all worked...</p>

<p>Car: If you don't have a car, get one. It is key to making this lifestyle work. You don't need to worry about a homeless shelter, and you can store all your possessions in it (all I had was a duffle bag, my laptop, sleeping stuff, and food). It's the one consistent place you can depend on. You'll be able to buy a cheap one for less than a month's rent in the Bay Area. I had a 2004 Honda Civic.</p>

<p>Sleeping: Sleeping is obviously super important if you expect to be in good mental standing everyday. I made it work by folding down the rear seats and laying a 3" foam mattress pad from the truck to the rear of the interior. I'm 6 feet tall and I could almost stretch out entirely while laying down. It's not as comfortable as a bed, but surprisingly, it's not as bad as you'd think. I slept fully through the nights.</p>

<p>Showering: I bought a 24 Hour Fitness membership to take care of my hygienic needs. Every morning I'd wake up and drive to the gym to take a shower. Then, like every other normal person, I'd head off to work. I found that having 24/7 access was really nice in case I had to go to the bathroom in the middle of the night. I'd always park within a few miles (and many times as close as the parking lot). If you're looking for a less expensive option, there are some gyms that are as low as $15/mo.</p>

<p>Working and free time: I signed up for a Hacker Dojo co-working membership. This is primarily where I spent all my time. They provide fast internet, a microwave, coffee, water, couches, community, etc. For only $100/mo, it's a no brainer. **Do not take advantage of their space. Don't use it to sleep in or steal from. Just common sense.</p>

<p>Eating: I stored almost all my food in my car. The small amount of perishable food I did have, I used the Hacker Dojo refrigerators. These are communal, so I chose not to store much in them. However, I would use their microwave everyday to heat my meals. I wrote a short post about what I ate: http://kurtvarner.com/post/19746.... Mainly, just get stuff that doesn't need to be refrigerated. However, it is definitely a challenge to eat healthy, as high sodium is going to be prevalent in nearly all canned foods. Just be cautious.</p>

<p>Parking: Technically, Palo Alto is the only Bay Area city where it's legal to live from a vehicle. That said, I parked more nights in Mountain View over the course of the 4 months. I was only slightly disturbed once when a police officer spotlighted my car. I sat up from my back seat, looked at him, and then he simply drove away.</p>

<p>When looking for locations to park, my focus was on being unnoticed. Anywhere with little foot traffic is best. Not sure where you're located, but here are a few places that worked well for me: https://maps.google.com/maps/ms?.... There are countless other places that will work well. I found these by taking an afternoon to just randomly drive around.</p>

<h2>Privacy</h2>
<p>Tint your windows: If you want your privacy, tint your windows. This was one of the best decisions I made, as it really helped me to fly under the radar. I went with a 23% tint that cost about $120 professionally installed. It wasn't pitch black, but it was dark enough where you couldn't see in during the night, and made you and your possessions not obvious during the day.</p>
<p>Sun shade: Get a $5 sun shade to put in the windshield while you sleep.</p>
<p>Rear screen: Do the same with a mesh screen for the rear window.</p>

