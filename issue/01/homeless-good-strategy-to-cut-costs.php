<p>Overall, this lifestyle begins to feel fairly normal. Admittedly, it's nicer to be living in an apartment, but if you're in desperate need to cut costs, a car will be your best friend.</p>

<p>On a side note, there's another fantastic benefit that comes from living like this - embracing a minimalistic perspective on life. Before these 4 months, everyday I took for granted things like a warm bed, shower, home cooked meals, etc. Itâ€™s easy to lose sight of how privileged the majority of us are, but there are many, many people without these basic things. I realized that even the simple things in life could bring me more happiness than a world of possessions.</p>

<p>Since starting my journey, I've heard a lot of encouragement from the startup community, but also quite a bit of hatred. You have to take the negativity with a grain of salt. You know your situation better than anyone, so just trust yourself.</p>

<p>You are not alone. During my experience I saw many other people living from vehicles. It's strange that most people are oblivious to it. There are even several other entrepreneurs I know that are taking to the streets to cut costs here in the Valley.</p>

