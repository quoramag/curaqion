    <li>108 number also figures prominently in the symbolism associated with karate, particularly the Gōjū-ryū discipline. The ultimate Gōjū-ryū kata, Suparinpei, literally translates to 108. Suparinpei is the Chinese pronunciation of the number 108, while gojūshi of Gojūshiho is the Japanese pronunciation of the number 54. The other Gōjū-ryū kata, Sanseru (meaning "36") and Seipai ("18") are factors of the number 108.</li>
