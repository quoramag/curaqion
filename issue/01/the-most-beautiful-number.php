<h2>Religion and the arts:</h2>

<p>The individual numbers 1, 0, and 8 represent one thing, nothing, and everything(infinity).</p>

<p>108 represents the ultimate reality of the universe as being (seemingly paradoxically) simultaneously One, emptiness, and infinite.</p>

<h3>In Hinduism:</h3>

<p>Hindu deities have 108 names, while in Gaudiya Vaishnavism, there are 108 gopis of Vrindavan. Recital of these names, often accompanied by counting of 108-beaded Mala, is considered sacred and often done during religious ceremonies. The recital is called namajapa. Accordingly, a mala usually has beads for 108 repetitions of a mantra.</p>

<p>The distance of the Sun from the Earth divided by the diameter of the Sun and the distance of the Moon from the Earth divided by the diameter of the Moon is approximately equal to 108. It is claimed that the great sires of Vedanta knew this relationship and thus 108 is a very important number in Vedantic chantings.</p>

<h3>Martial Arts:</h3>

<p>Many East Asian martial arts trace their roots back to Buddhism, specifically, to the Buddhist Shaolin Temple. Because of their ties to Buddhism, 108 has become an important symbolic number in a number of martial arts styles.</p>

